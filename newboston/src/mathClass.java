public class mathClass {
    public static void main(String args[])
    {
        //abs= absolute value/mod
        System.out.println(Math.abs(-26.8));
        System.out.println(Math.abs(26.8));

        //ceil= up approximation
        System.out.println(Math.ceil(26.7));
        System.out.println(Math.ceil(26.3));

        //floor= down approximation
        System.out.println(Math.floor(26.7));
        System.out.println(Math.floor(26.3));

        //max= maximum
        System.out.println(Math.max(5.8,9.7));
        //min= minimum

        //pow(5,3)
        //sqrt(625)
    }
}
